package com.pinganzhiyuan.model;

public class DeviceStatisticalData {
	private int countNumber;
	private Long channelId;
	private Long appProductId;
	private String channelName;
	private String appProductName;
	public int getCountNumber() {
		return countNumber;
	}
	public void setCountNumber(int countNumber) {
		this.countNumber = countNumber;
	}
	public Long getChannelId() {
		return channelId;
	}
	public void setChannelId(Long channelId) {
		this.channelId = channelId;
	}
	public Long getAppProductId() {
		return appProductId;
	}
	public void setAppProductId(Long appProductId) {
		this.appProductId = appProductId;
	}
	public String getChannelName() {
		return channelName;
	}
	public void setChannelName(String channelName) {
		this.channelName = channelName;
	}
	public String getAppProductName() {
		return appProductName;
	}
	public void setAppProductName(String appProductName) {
		this.appProductName = appProductName;
	}
	
}
